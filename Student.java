package Day18practice;

import java.util.Arrays;

public class Student {


	private String name;
	private int rollNumber;
	private int[] marks ;

	Student() {
		this.name = "Unknown";
		this.rollNumber = 0;
		this.marks = new int[] {0,0,0};
		
	}

	Student(String name, int rollnum, int[] marks) {
		this.name = name;
		this.rollNumber = rollnum;
		this.marks=Arrays.copyOf(marks, 3);
	}

	public void displayDetails() {
		System.out.println("Student details:\n" + "student name : " + this.name + "\n" + "rollNumber : "
				+ this.rollNumber + "\n" + "marks : " + marks[0] + "  " + marks[1] + "  " + marks[2]);
	}

	public int calculateTotal() {
		int sum = marks[0] + marks[1] + marks[2];
		return sum;

	}

	public double calculateAverage() {
		double avgmarks = calculateTotal() / 3.0;

		return avgmarks;
	}

}

